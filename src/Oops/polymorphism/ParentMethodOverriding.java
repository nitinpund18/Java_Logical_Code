package Oops.polymorphism;

public class ParentMethodOverriding 
{
	public void bunglow()
	{
		System.out.println("Parent Bunglow=02");
	}
	public void farm()
	{
		System.out.println("Parent car=01");
	}
	public void addition(int a, int b)
	{
		int sum=a+b;
		System.out.println("Parent sum="+sum);
	}
}
